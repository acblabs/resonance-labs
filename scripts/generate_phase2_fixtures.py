def write_cross_language_chirp_fixture() -> None:
    sample_rate_hz = 8000
    config = {
        "signal_type": "log_chirp",
        "start_hz": 300,
        "end_hz": 2400,
        "duration_ms": 100,
        "pre_roll_ms": 0,
        "post_roll_ms": 100,
        "amplitude": 0.35,
        "fade_ms": 10,
    }
    samples = generate_log_chirp(
        ChirpSpec(
            start_hz=float(config["start_hz"]),
            end_hz=float(config["end_hz"]),
            duration_seconds=float(config["duration_ms"]) / 1000.0,
            amplitude=float(config["amplitude"]),
            fade_seconds=float(config["fade_ms"]) / 1000.0,
        ),
        sample_rate_hz,
    ).astype(np.float32)

    payload = {
        "name": "cross_language_log_chirp_8khz_100ms",
        "description": (
            "Selected golden Float32 chirp samples generated by the Python DSP reference. "
            "Used by Python and TypeScript tests to keep browser playback and server "
            "matched-filter reference aligned, especially at cosine fade endpoints."
        ),
        "sample_rate_hz": sample_rate_hz,
        "config": config,
        "atol": 1e-7,
        "sample_indices": CROSS_LANGUAGE_SAMPLE_INDICES,
        "samples": [float(samples[index]) for index in CROSS_LANGUAGE_SAMPLE_INDICES],
    }
    write_json(FIXTURES_DIR / "cross_language_log_chirp.json", payload)


