def build_recorded_style_probe(
    *,
    sample_rate_hz: int,
    pre_roll_seconds: float,
    chirp_seconds: float,
    post_roll_seconds: float,
    acoustic_delay_seconds: float,
    start_hz: float,
    end_hz: float,
    amplitude: float,
    fade_seconds: float,
    noise_seed: int,
) -> np.ndarray:
    rng = np.random.default_rng(noise_seed)
    total_seconds = pre_roll_seconds + chirp_seconds + post_roll_seconds + 0.06
    sample_count = int(round(total_seconds * sample_rate_hz))
    time = np.arange(sample_count, dtype=np.float64) / sample_rate_hz
    samples = rng.normal(0.0, 0.0014, sample_count)
    samples += 0.0016 * np.sin(2.0 * math.pi * 60.0 * time)
    samples += 0.0007 * np.sin(2.0 * math.pi * 180.0 * time + 0.4)

    chirp = generate_log_chirp(
        ChirpSpec(start_hz, end_hz, chirp_seconds, amplitude, fade_seconds),
        sample_rate_hz,
    )
    colored = color_chirp(chirp, sample_rate_hz)
    chirp_start = int(round((pre_roll_seconds + acoustic_delay_seconds) * sample_rate_hz))

    add_at(samples, colored, chirp_start, gain=1.0)
    add_at(samples, colored, chirp_start + int(round(0.024 * sample_rate_hz)), gain=0.24)
    add_at(
        samples,
        np.convolve(colored, np.ones(9) / 9.0, mode="same"),
        chirp_start + int(round(0.061 * sample_rate_hz)),
        gain=0.11,
    )

    ring_start = chirp_start + chirp.size + int(round(0.014 * sample_rate_hz))
    ring_time = np.arange(samples.size - ring_start, dtype=np.float64) / sample_rate_hz
    ring = 0.062 * np.exp(-4.9 * ring_time) * np.sin(2.0 * math.pi * 1640.0 * ring_time + 0.2)
    ring += 0.034 * np.exp(-7.5 * ring_time) * np.sin(2.0 * math.pi * 2925.0 * ring_time - 0.3)
    ring += 0.018 * np.exp(-12.0 * ring_time) * np.sin(2.0 * math.pi * 4810.0 * ring_time + 1.1)
    samples[ring_start:] += ring

    return np.clip(np.tanh(1.22 * samples) / 1.22, -0.98, 0.98)


def color_chirp(chirp: np.ndarray, sample_rate_hz: int) -> np.ndarray:
    frequencies = np.fft.rfftfreq(chirp.size, d=1.0 / sample_rate_hz)
    magnitude = 0.56 / np.maximum(1.0, (frequencies / 800.0) ** 0.12)
    magnitude *= 1.0 - 0.32 * np.exp(-0.5 * ((frequencies - 3600.0) / 520.0) ** 2)
    magnitude *= 1.0 - 0.22 * np.exp(-0.5 * ((frequencies - 7800.0) / 840.0) ** 2)
    magnitude *= 1.0 + 0.055 * np.cos(2.0 * math.pi * frequencies * 0.0018)
    phase_shift = np.exp(-1j * 2.0 * math.pi * frequencies * 0.00055)
    colored = np.fft.irfft(np.fft.rfft(chirp) * magnitude * phase_shift, n=chirp.size)
    return colored + 0.018 * np.power(colored, 3)


def add_at(target: np.ndarray, signal: np.ndarray, start: int, *, gain: float) -> None:
    end = min(target.size, start + signal.size)
    if end > start:
        target[start:end] += gain * signal[: end - start]


def write_json(path: Path, payload: object) -> None:
    path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")


